exports.getCoinsLevel = (coinsCount) => {
    return new Promise((resolve, reject) => {
        var level = 1
        coins.forEach((item) => {
            if (coinsCount >= item.coins) {
                level = item.level;
            }
        });
        if (level == undefined) {
            reject("An error occurred while getting coins level");
        } else {
            resolve(level);
        }
    });
}

exports.getGemsLevel = (gemsCount) => {
    return new Promise((resolve, reject) => {
        var level = 0;
        gems.forEach((item) => {
            if (gemsCount >= item.gems) {
                level = item.level;
            }
        });
        if (level == undefined) {
            console.log("level in reject", level)
            reject("An error occurred while getting gems level");
        } else {
            console.log("level in resolve", level)
            resolve(level);
        }
    });
}